$(function(){ 
	{
$(':checkbox[name=1]').each(function(){ 
$(this).click(function(){ 
if($(this).attr('checked')){ 
$(':checkbox[name=1]').removeAttr('checked'); 
$(this).attr('checked','checked'); 
} 
}); 
}); 

}}); 



